#include "libft.h"

char	*ft_strnstr(const char *big, const char *little, size_t len)
{
	size_t	i;
	size_t	j;

	i = 0;
	if (!little[i])
		return ((char *)big);
	while (big[i] && i < len)
	{
		j = 0;
		while (big[i + j] == little[j] && little[j])
		{
			if (i + j >= len)
				break ;
			if (!little[j + 1])
				return (&((char *)big)[i]);
			j++;
		}
		i++;
	}
	return (NULL);
}
